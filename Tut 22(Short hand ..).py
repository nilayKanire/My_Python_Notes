'''
Short Hand If Else Notation In Python
'''

a = int(input("enter a\n"))
b = int(input("enter b\n"))

if a>b: print("A B se bada hai bhai")
print("B A se bada hai bhai") if a<b else print("A B se bada hai bhai")                  #    THis is short hand if else statement
                                                                                         # through this we can write code in lesser lines.